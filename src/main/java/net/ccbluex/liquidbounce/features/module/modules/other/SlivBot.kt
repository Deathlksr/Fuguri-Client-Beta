    private var lastUsed = 0
    private val msTimer: MSTimer = MSTimer()

    @EventTarget
    fun onUpdate(event: UpdateEvent?) {
        if (msTimer.hasTimePassed((delayValue.get() * 1000).toLong())) {
            if (emotionsValue.get()) {
                mc.thePlayer.sendChatMessage(userName.get() + randomPhrase() + ")")
                msTimer.reset()
            } else if (msTimer.hasTimePassed((delayValue.get() * 1000).toLong())) {
                mc.thePlayer.sendChatMessage(userName.get() + randomPhrase())
                msTimer.reset()
            }
        }
    }

    private fun randomPhrase(): String {
        val rand = Random()

        var randInt: Int
        randInt = rand.nextInt(phraseList.size)
        while (lastUsed == randInt) {
            randInt = rand.nextInt(phraseList.size)
        }

        lastUsed = randInt
        return phraseList[randInt]
    }

    private fun randomDelay(): Int {
        val randy = Random()
        val randyInt = randy.nextInt(4100) + 4100
        return randyInt
    }
